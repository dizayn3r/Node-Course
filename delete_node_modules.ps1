# Function to delete node_modules folder if it exists
function DeleteNodeModules {
    param(
        [string]$path
    )

    $nodeModulesPath = Join-Path $path "node_modules"

    if (Test-Path $nodeModulesPath) {
        Remove-Item $nodeModulesPath -Recurse -Force
        Write-Host "Deleted node_modules at: $nodeModulesPath"
    } else {
        Write-Host "node_modules not found at: $path"
    }
}

# Prompt the user for the path
Write-Host "Enter the path to the directory where you want to delete node_modules:"
$path = Read-Host

# Call the function with the specified path
DeleteNodeModules $path